    public ProChooserSetOps(ClassroomHelper appl, ClassS cla, ClassHome home, JPanel last, GUIFrame frame, String ty) {
        app = appl;
        classS = cla;
        homepage = home;
        lastPage = last;
        baseFrame = frame;
        type = ty;
        prompts = new ArrayList<Prompt>();
        if(type.equals("SAQ")) {
            for(int i = 0; i < classS.getSAQs().size(); i++) {
               if(!classS.getSAQs().get(i).getIsAns()) {
                   prompts.add(classS.getSAQs().get(i));
               }
            }
        } else {
            for(int i = 0; i < classS.getERQs().size(); i++) {
               if(!classS.getERQs().get(i).getIsAns()) {
                   prompts.add(classS.getERQs().get(i));
               }
            }
        }
        baseFrame.setContentPane(this);
        for(Prompt p: prompts) {
            System.out.println(p.getQue());
        }
        initComponents();
        addProsToList();
        okayB.setEnabled(false);
        jList1.setSelectionModel(new javax.swing.DefaultListSelectionModel() {
            @Override
            public void setSelectionInterval(int index0, int index1) {
                if(super.isSelectedIndex(index0)) {
                    //if the item that was clicked was already selected, deselect it
                    super.removeSelectionInterval(index0, index1);
                } else {
                    //add the item that was clicked to the selection
                    super.addSelectionInterval(index0, index1);
                }
                
                okayB.setEnabled(!super.isSelectionEmpty());
            }
        });
    }

