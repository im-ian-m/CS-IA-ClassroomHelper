    public void addProsToList() {
        model = new DefaultListModel();
        data = new String[prompts.size()];
        
        for(int i = 0; i < prompts.size(); i++) {
            data[i] = prompts.get(i).getQue();
            model.add(i, data[i]);
        }
        
        jList1.setModel(model);
    }
    
    

